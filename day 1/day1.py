# Syntax, Comments and Statement in python

# Syntax is the set of rules that defines how a Python program will be written and interpreted. It includes rules for writing code, such as how to define variables, how to write functions, and how to use control structures like loops and conditionals.

# Comments are lines in a program that are not executed by the interpreter. They are used to explain the code and make it easier to understand. In Python, comments start with the # symbol.

""" This is a multi-line comment in Python. It can be used to provide more detailed explanations or to comment out blocks of code. """

# Statements are instructions that the Python interpreter can execute. They can be simple, like assigning a value to a variable, or more complex, like defining a function or creating a loop.

