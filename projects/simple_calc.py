# Simple Project I (Mini Calculator in Python)

num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))

add = num1 + num2
subtract = num1 - num2
multiply = num1 * num2
divide = num1 / num2
exponent = num1 ** num2
rem = num1 % num2
print("Addition: ", add)
print("Subtraction: ", subtract)
print("Multiplication: ", multiply)
print("Division: ", divide)
print("Exponent: ", exponent)
print("Remainder: ", rem)