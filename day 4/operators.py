# Logical operators
print(True and False) # Logical AND
print(True or False) # Logical OR
print(not True) # Logical NOT

print("------------")
print(5 > 3 and 2 < 4) # True and True -> True
print(5 > 3 or 2 > 4) # True or False -> True
print(not (5 > 3)) # not True -> False